package org.zodiark.service.wowza;

public class WowzaUUID {

    private String uuid;

    public WowzaUUID(){}

    public WowzaUUID(String uuid) {
        this.uuid = uuid;
    }

    public String getUuid() {
        return uuid;
    }

    public void setUuid(String uuid) {
        this.uuid = uuid;
    }
}

